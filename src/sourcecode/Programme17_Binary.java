package sourcecode;

public class Programme17_Binary {

    /*
     * 17. Write a Java program to convert a decimal number to binary number.
     * Input Data:
     * Input a Decimal Number : 5
     * Expected output
     * Binary number is: 101
     */
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Binary number for two given input is: " + Integer.toBinaryString(a));
    }
}

